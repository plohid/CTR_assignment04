// 2. Convert Minutes into Seconds

// Write a function that takes an integer minutes
//  and converts it to seconds.

function minToSeconds(minutes){
    let seconds = minutes*60;

    return seconds;
}


// minToSeconds(5)

console.log(minToSeconds(5));
console.log(minToSeconds(3));
console.log(minToSeconds(2));


